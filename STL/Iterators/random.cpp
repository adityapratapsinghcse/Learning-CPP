#include <iostream>
#include <vector>
using namespace std;

int main()
{
    // ==================================================
    // CREATION
    // ==================================================

    vector<int> numbers =
    {
        10,20,30,40,50
    };

    // ==================================================
    // ITERATOR CREATION
    // ==================================================

    vector<int>::iterator it;

    // ==================================================
    // TRAVERSAL
    // ==================================================

    cout << "Traversal : ";

    for(it = numbers.begin();
        it != numbers.end();
        ++it)
    {
        cout << *it << " ";
    }

    cout << endl;

    // ==================================================
    // READING
    // ==================================================

    it = numbers.begin();

    cout << "\nCurrent Value : "
         << *it
         << endl;

    // ==================================================
    // WRITING
    // ==================================================

    *it = 100;

    cout << "After Modification : "
         << *it
         << endl;

    // ==================================================
    // FORWARD MOVEMENT
    // ==================================================

    ++it;

    cout << "After ++it : "
         << *it
         << endl;

    // ==================================================
    // BACKWARD MOVEMENT
    // ==================================================

    --it;

    cout << "After --it : "
         << *it
         << endl;

    // ==================================================
    // JUMP FORWARD
    // ==================================================

    cout << "\n*(it + 3) : "
         << *(it + 3)
         << endl;

    // ==================================================
    // JUMP BACKWARD
    // ==================================================

    it = numbers.end() - 1;

    cout << "Last Element : "
         << *it
         << endl;

    cout << "*(it - 2) : "
         << *(it - 2)
         << endl;

    // ==================================================
    // += OPERATOR
    // ==================================================

    it = numbers.begin();

    it += 2;

    cout << "\nAfter it += 2 : "
         << *it
         << endl;

    // ==================================================
    // -= OPERATOR
    // ==================================================

    it -= 1;

    cout << "After it -= 1 : "
         << *it
         << endl;

    // ==================================================
    // INDEXING
    // ==================================================

    it = numbers.begin();

    cout << "\nUsing it[0] : "
         << it[0]
         << endl;

    cout << "Using it[3] : "
         << it[3]
         << endl;

    // ==================================================
    // DISTANCE BETWEEN ITERATORS
    // ==================================================

    auto first = numbers.begin();
    auto last  = numbers.end();

    cout << "\nDistance : "
         << last - first
         << endl;

    // ==================================================
    // COMPARISON OPERATORS
    // ==================================================

    auto a = numbers.begin();
    auto b = numbers.begin() + 3;

    if(a < b)
    {
        cout << "a comes before b"
             << endl;
    }

    if(b > a)
    {
        cout << "b comes after a"
             << endl;
    }

    // ==================================================
    // MULTIPLE PASSES
    // ==================================================

    cout << "\nSecond Traversal : ";

    for(auto itr = numbers.begin();
        itr != numbers.end();
        itr++)
    {
        cout << *itr << " ";
    }

    cout << endl;
    return 0;
}